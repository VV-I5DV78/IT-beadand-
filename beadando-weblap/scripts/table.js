function buildTable(jsonData) {
    const container = document.getElementById("tablazatDiv");

    //OSZLOPOK
    const appNames = jsonData.map(app => app.name);
    
    const categories = Object.keys(jsonData[0]).filter(k => k !== "name");


    const table = document.createElement("table");
    
    const tcaption = document.createElement("caption");
    tcaption.textContent = "Chat appokat összehasonlító táblázat";

    table.appendChild(tcaption);

    //FEJLÉC
    const tblhead = document.createElement("thead");
    const headerRow = document.createElement("tr");
    tblhead.classList.add("first")
    
    const emptyTh = document.createElement("th");
    emptyTh.textContent = "";  // bal felső sarok
    headerRow.appendChild(emptyTh);

    appNames.forEach(name => {
        const th = document.createElement("th");
        const logo = document.createElement("img");
        th.textContent = name;

        switch (name){
          case "Signal":
            console.log("signalmuv");
            th.classList.add("tmp");
            logo.src = "../media/signal-logo.jpg"
            break;
          case "Viber":
            logo.src = "../media/viber-logo.jpg"
            break;
          case "Threema":
            logo.src = "../media/threema-logo.jpg"
            break;
          case "WhatsApp":
            logo.src = "../media/whatsapp-logo.jpg"
            break;
          case "Discord":
            logo.src = "../media/discord-logo.jpg"
            break;
          case "Session":
            logo.src = "../media/session-logo.jpg"
            break;
          case "Messenger":
            logo.src = "../media/messenger-logo.jpg"
            break;
          case "SnapChat":
            logo.src = "../media/snapchat-logo.jpg"
            break;
        }
        th.appendChild(logo);

        headerRow.appendChild(th);
    });

    tblhead.appendChild(headerRow)
    table.appendChild(tblhead);

    const tblbody = document.createElement("tbody");

    //NAGY KATEGÓRIÁK
    categories.forEach(category => {
        // Kategória címsor (egy teljes szélességű sor)
        const catRow = document.createElement("tr");
        const catCell = document.createElement("td");

        catCell.colSpan = appNames.length + 1;
        catCell.textContent = category;
        catCell.style.fontWeight = "bold";
        catCell.style.background = "#bbb";

        catRow.appendChild(catCell);
        tblbody.appendChild(catRow);

        //KATEGÓRIÁN BELÜL
        const rowKeys = Object.keys(jsonData[0][category]);

        rowKeys.forEach(rowKey => {
            const tr = document.createElement("tr");

            // Első cella = a sor neve (pl "Audio-/Video-chats")
            const labelCell = document.createElement("td");
            labelCell.textContent = rowKey;
            tr.appendChild(labelCell);

            // A többi cella = érték + szín (class)
            jsonData.forEach(app => {
                const td = document.createElement("td");
                const item = app[category][rowKey];

                td.textContent = item.value;
                td.classList.add(item.color); // pl .green, .red, stb.

                tr.appendChild(td);
            });

            tblbody.appendChild(tr);
        });
    });
    table.appendChild(tblbody);
    container.appendChild(table);
}


buildTable(jsonData);

